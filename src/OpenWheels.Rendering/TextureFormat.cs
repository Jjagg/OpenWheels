namespace OpenWheels.Rendering
{
    /// <summary>
    /// The color formats supported by OpenWheels.
    /// </summary>
    public enum TextureFormat
    {
        /// <summary>
        /// 8-bit unsigned normalized red, green, blue and alpha channel.
        /// </summary>
        Rgba32,
        /// <summary>
        /// 8-bit unsigned normalized red channel only.
        /// </summary>
        Red8
    }
}
