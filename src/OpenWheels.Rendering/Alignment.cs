namespace OpenWheels.Rendering
{
    /// <summary>
    /// Alignement options for text rendering.
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// Align the start of the text with the given position.
        /// </summary>
        Start,
        /// <summary>
        /// Align the center of the text with the given position.
        /// </summary>
        Center,
        /// <summary>
        /// Align the end of the text with the given position.
        /// </summary>
        End
    }
}
